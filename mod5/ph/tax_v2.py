func = int(input('Напишите ваш доход: '))

if(func <= 0):
    print('Нет дохода, нет налога')
elif (func <= 10000):
    print('Налог составляет: ', func * 0.13)
elif 10000 < func <= 50000:
    print('Налог составляет: ', 10000 * 0.13 + (func - 10000) * 0.2)
elif func > 50000:
    print('Налог составляет: ', 0.13 * 10000 + 0.2 * (50000 - 10000) + 0.3 * (func - 50000))